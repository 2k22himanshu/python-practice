# Variables in Python

# String variable
name = "Himanshu"

# Integer variable
age = 21

# Float variable
height = 5.9

# Boolean variable
is_student = True

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Student Status:", is_student)

# Updating variable value
age = 22

print("Updated Age:", age)
